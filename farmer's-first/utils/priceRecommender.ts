interface CropDataRecord {
  type: CulturalPractice;
  cropType: string;
  price: number;
}

// Memoization cache for processed data to avoid reprocessing on every call.
let processedData: {
  avgPriceByTypeAndCrop: Map<string, number>;
  avgPriceByCrop: Map<string, number>;
  overallAvgPrice: number;
  uniqueCropTypes: string[];
} | null = null;

/**
 * Parses the raw CSV data and computes average prices.
 * This is a one-time operation, memoized for performance.
 */
function getProcessedData() {
  if (processedData) {
    return processedData;
  }

  const lines = csvData.trim().split('\n');
  const headers = lines[0].split(',');
  const records: CropDataRecord[] = [];

  const typeIndex = headers.indexOf('Type');
  const cropTypeIndex = headers.indexOf('Crop Type');
  const priceIndex = headers.indexOf('Price (INR/kg)');

  for (let i = 1; i < lines.length; i++) {
    const values = lines[i].split(',');
    if (values.length >= headers.length) {
      const type = values[typeIndex] as CulturalPractice;
      // Ensure the type is a valid CulturalPractice enum value
      if (Object.values(CulturalPractice).includes(type)) {
        records.push({
          type: type,
          cropType: values[cropTypeIndex].trim(),
          price: parseFloat(values[priceIndex]),
        });
      }
    }
  }

  const priceMapByTypeAndCrop: Map<string, { total: number; count: number }> = new Map();
  const priceMapByCrop: Map<string, { total: number; count: number }> = new Map();
  let overallTotal = 0;

  records.forEach(record => {
    // By Type and Crop
    const key1 = `${record.type}-${record.cropType}`;
    const entry1 = priceMapByTypeAndCrop.get(key1) || { total: 0, count: 0 };
    entry1.total += record.price;
    entry1.count++;
    priceMapByTypeAndCrop.set(key1, entry1);

    // By Crop only
    const key2 = record.cropType;
    const entry2 = priceMapByCrop.get(key2) || { total: 0, count: 0 };
    entry2.total += record.price;
    entry2.count++;
    priceMapByCrop.set(key2, entry2);
    
    // Overall
    overallTotal += record.price;
  });

  const avgPriceByTypeAndCrop: Map<string, number> = new Map();
  priceMapByTypeAndCrop.forEach((value, key) => {
    avgPriceByTypeAndCrop.set(key, value.total / value.count);
  });

  const avgPriceByCrop: Map<string, number> = new Map();
  priceMapByCrop.forEach((value, key) => {
    avgPriceByCrop.set(key, value.total / value.count);
  });
  
  const overallAvgPrice = overallTotal / records.length;
  const uniqueCropTypes = [...priceMapByCrop.keys()];
  
  processedData = {
    avgPriceByTypeAndCrop,
    avgPriceByCrop,
    overallAvgPrice,
    uniqueCropTypes
  };

  return processedData;
}


/**
 * Simulates an ML model to recommend a market price for a crop based on historical data.
 * @param cropName The name of the crop input by the user.
 * @param culturalPractice The farming method used.
 * @returns A recommended price per kg in INR.
 */
export const getRecommendedPrice = (cropName: string, culturalPractice: CulturalPractice): number => {
  if (!cropName) {
    return 0;
  }
  
  const { 
    avgPriceByTypeAndCrop,
    avgPriceByCrop,
    overallAvgPrice,
    uniqueCropTypes
  } = getProcessedData();

  // Find the best matching crop type from our dataset
  const lowerCaseCropName = cropName.toLowerCase();
  let matchedCropType: string | null = null;

  // Prioritize exact match, then substring match
  for (const type of uniqueCropTypes) {
      if (lowerCaseCropName === type.toLowerCase()) {
          matchedCropType = type;
          break;
      }
  }
  if (!matchedCropType) {
      for (const type of uniqueCropTypes) {
          if (lowerCaseCropName.includes(type.toLowerCase()) || type.toLowerCase().includes(lowerCaseCropName)) {
              matchedCropType = type;
              break;
          }
      }
  }

  let basePrice: number | undefined;

  if (matchedCropType) {
    // 1. Try to get price for specific type and crop
    const key = `${culturalPractice}-${matchedCropType}`;
    basePrice = avgPriceByTypeAndCrop.get(key);

    // 2. Fallback to average for the crop type
    if (basePrice === undefined) {
      basePrice = avgPriceByCrop.get(matchedCropType);
    }
  }
  
  // 3. Fallback to overall average price
  if (basePrice === undefined) {
      basePrice = overallAvgPrice;
  }
  
  // Add a random fluctuation to simulate market variance (+/- 5%)
  const fluctuation = (Math.random() - 0.5) * 0.1; // from -0.05 to +0.05
  const recommendedPrice = basePrice * (1 + fluctuation);

  // Return a rounded, clean number, ensuring it's not nonsensically low
  return Math.max(1, Math.round(recommendedPrice));
};
